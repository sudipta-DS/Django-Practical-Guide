def get_data(post):
    return post['date']

def get_single_post(slug):
    for post in sorted_posts:
        if post["slug"] == slug :
            return post
        else:
            continue
sorted_posts = sorted(all_posts,key=get_data)
latest_posts = sorted_posts[-3:]

def starting_page(request):
    return render(request,'posts/index.html',{
        "posts":latest_posts
    })

def all_post(request):
    return render(request,'posts/all-posts.html',{
        "posts":sorted_posts
    })

def post_details(request,slug):
    specific_post = next(post for post in all_posts if post['slug']==slug )
    return render(request,'posts/post-detail.html',{
        "post":specific_post
    })